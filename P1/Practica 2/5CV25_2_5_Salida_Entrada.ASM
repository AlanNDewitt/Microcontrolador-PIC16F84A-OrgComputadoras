#include "P16F84.inc" ;Libreria para el pic
TEMP EQU 0XC
		  
BSF STATUS,5 	   ; IR A LA PÁGINA1 o Bloque de memoria 1
MOVLW b'00011111'
MOVWF TRISA		 ;Definimos los primeros 5 pines del puerto A como salida

MOVLW b'00000000'
MOVWF TRISB    ;Definimos el puerto B como salida 

BCF STATUS,5 ;IR A PÁGINA 0 o banco de memoria 0



BUCLE
	CLRF PORTA
	CLRF PORTB ;INICIO DEL PORTB A CERO
	
	BTFSC PORTA,0
		GOTO B0
	BTFSC PORTA,1
		GOTO B1
	BTFSC PORTA,2
		GOTO B2
	BTFSC PORTA,3
		GOTO B3
	BTFSC PORTA,4
		GOTO B4
	GOTO BUCLE
	
B0
	MOVLW b'00000001'
	MOVWF PORTB
	GOTO BUCLE
		 
B1
	MOVLW b'00000010'
	MOVWF PORTB
	GOTO BUCLE

B2
	MOVLW b'00000100'
	MOVWF PORTB
	GOTO BUCLE

B3
	MOVLW b'00001000'
	MOVWF PORTB
	GOTO BUCLE
	
B4
	MOVLW b'00010000'
	MOVWF PORTB
	GOTO BUCLE
	
END